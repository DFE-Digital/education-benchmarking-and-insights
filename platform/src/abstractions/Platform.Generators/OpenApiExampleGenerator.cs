using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Platform.Generators
{
    [Generator]
    public class OpenApiExampleGenerator : IIncrementalGenerator
    {
        private const string BaseAttributeNamespace = "Platform.OpenApi.Attributes";
        private static readonly string[] TargetAttributes =
        {
            $"{BaseAttributeNamespace}.GenerateOpenApiExampleAttribute",
            $"{BaseAttributeNamespace}.GenerateOpenApiPropertiesExampleAttribute",
            $"{BaseAttributeNamespace}.GenerateOpenApiStringValuesExampleAttribute",
            $"{BaseAttributeNamespace}.GenerateOpenApiIntValuesExampleAttribute"
        };

        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            var classDeclarations = context.SyntaxProvider
                .CreateSyntaxProvider(
                    predicate: static (s, _) => s is ClassDeclarationSyntax c && c.AttributeLists.Count > 0,
                    transform: static (ctx, _) => GetSemanticTargetForGeneration(ctx))
                .Where(static m => m is not null);

            var compilationAndClasses = context.CompilationProvider.Combine(classDeclarations.Collect());

            context.RegisterSourceOutput(compilationAndClasses,
                static (spc, source) => Execute(source.Left, source.Right, spc));
        }

        private static ClassDeclarationSyntax? GetSemanticTargetForGeneration(GeneratorSyntaxContext context)
        {
            var classDeclaration = (ClassDeclarationSyntax)context.Node;
            foreach (var attributeListSyntax in classDeclaration.AttributeLists)
            {
                foreach (var attributeSyntax in attributeListSyntax.Attributes)
                {
                    if (context.SemanticModel.GetSymbolInfo(attributeSyntax).Symbol is IMethodSymbol attributeSymbol)
                    {
                        string attributeName = attributeSymbol.ContainingType.ToDisplayString();
                        if (TargetAttributes.Contains(attributeName))
                        {
                            return classDeclaration;
                        }
                    }
                }
            }
            return null;
        }

        private static void Execute(Compilation compilation, ImmutableArray<ClassDeclarationSyntax?> classes, SourceProductionContext context)
        {
            if (classes.IsDefaultOrEmpty) return;

            foreach (var classDeclaration in classes.Distinct())
            {
                if (classDeclaration is null) continue;

                var semanticModel = compilation.GetSemanticModel(classDeclaration.SyntaxTree);
                if (semanticModel.GetDeclaredSymbol(classDeclaration) is not INamedTypeSymbol classSymbol) continue;

                var namespaceName = classSymbol.ContainingNamespace.ToDisplayString();
                var className = classSymbol.Name;
                var isStatic = classSymbol.IsStatic;
                var hintName = $"{namespaceName}.{className}.g.cs";

                var attributes = classSymbol.GetAttributes()
                    .Where(a => TargetAttributes.Contains(a.AttributeClass?.ToDisplayString()));

                var sb = new StringBuilder();
                sb.AppendLine("// <auto-generated/>");
                sb.AppendLine("using Microsoft.Azure.WebJobs.Extensions.OpenApi.Core.Abstractions;");
                sb.AppendLine("using Microsoft.Azure.WebJobs.Extensions.OpenApi.Core.Resolvers;");
                sb.AppendLine("using Newtonsoft.Json.Serialization;");
                sb.AppendLine("using System.Diagnostics.CodeAnalysis;");
                sb.AppendLine($"namespace {namespaceName}");
                sb.AppendLine("{");
                sb.AppendLine($"    public {(isStatic ? "static " : "")}partial class {className}");
                sb.AppendLine("    {");

                foreach (var attr in attributes)
                {
                    var attributeName = attr.AttributeClass?.ToDisplayString();
                    var namedArgs = attr.NamedArguments.ToImmutableDictionary();

                    if (!namedArgs.TryGetValue("Name", out var nameArg) || nameArg.IsNull) continue;
                    string name = (string)nameArg.Value!;

                    if (attributeName == $"{BaseAttributeNamespace}.GenerateOpenApiExampleAttribute")
                    {
                        GenerateCollectionExample(sb, name, namedArgs);
                    }
                    else if (attributeName == $"{BaseAttributeNamespace}.GenerateOpenApiPropertiesExampleAttribute")
                    {
                        GeneratePropertiesExample(sb, name, namedArgs);
                    }
                    else if (attributeName == $"{BaseAttributeNamespace}.GenerateOpenApiStringValuesExampleAttribute")
                    {
                        GenerateValuesExample(sb, name, namedArgs, "string");
                    }
                    else if (attributeName == $"{BaseAttributeNamespace}.GenerateOpenApiIntValuesExampleAttribute")
                    {
                        GenerateValuesExample(sb, name, namedArgs, "int");
                    }
                }

                sb.AppendLine("    }");
                sb.AppendLine("}");

                context.AddSource(hintName, SourceText.From(sb.ToString(), Encoding.UTF8));
            }
        }

        private static void GenerateCollectionExample(StringBuilder sb, string name, ImmutableDictionary<string, TypedConstant> namedArgs)
        {
            if (!namedArgs.TryGetValue("SourceType", out var sourceTypeArg) ||
                !namedArgs.TryGetValue("SourceProperty", out var sourcePropArg) ||
                sourceTypeArg.IsNull || sourcePropArg.IsNull) return;

            ITypeSymbol sourceType = (ITypeSymbol)sourceTypeArg.Value!;
            string sourceProp = (string)sourcePropArg.Value!;

            sb.AppendLine($"        [ExcludeFromCodeCoverage]");
            sb.AppendLine($"        public class {name} : OpenApiExample<string>");
            sb.AppendLine("        {");
            sb.AppendLine("            public override IOpenApiExample<string> Build(NamingStrategy namingStrategy = null!)");
            sb.AppendLine("            {");
            sb.AppendLine($"                foreach (var val in {sourceType.ToDisplayString()}.{sourceProp})");
            sb.AppendLine("                {");
            sb.AppendLine("                    Examples.Add(OpenApiExampleResolver.Resolve(val, val, namingStrategy));");
            sb.AppendLine("                }");
            sb.AppendLine("                return this;");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
        }

        private static void GeneratePropertiesExample(StringBuilder sb, string name, ImmutableDictionary<string, TypedConstant> namedArgs)
        {
            if (!namedArgs.TryGetValue("SourceType", out var sourceTypeArg) ||
                !namedArgs.TryGetValue("Properties", out var propsArg) ||
                sourceTypeArg.IsNull || propsArg.Values.IsDefaultOrEmpty) return;

            ITypeSymbol sourceType = (ITypeSymbol)sourceTypeArg.Value!;
            var properties = propsArg.Values.Select(v => (string)v.Value!).ToList();

            sb.AppendLine($"        [ExcludeFromCodeCoverage]");
            sb.AppendLine($"        public class {name} : OpenApiExample<string>");
            sb.AppendLine("        {");
            sb.AppendLine("            public override IOpenApiExample<string> Build(NamingStrategy namingStrategy = null!)");
            sb.AppendLine("            {");
            foreach (var prop in properties)
            {
                sb.AppendLine($"                Examples.Add(OpenApiExampleResolver.Resolve({sourceType.ToDisplayString()}.{prop}, {sourceType.ToDisplayString()}.{prop}, namingStrategy));");
            }
            sb.AppendLine("                return this;");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
        }

        private static void GenerateValuesExample(StringBuilder sb, string name, ImmutableDictionary<string, TypedConstant> namedArgs, string type)
        {
            if (!namedArgs.TryGetValue("Labels", out var labelsArg) ||
                !namedArgs.TryGetValue("Values", out var valuesArg) ||
                labelsArg.Values.Length != valuesArg.Values.Length) return;

            var labels = labelsArg.Values.Select(v => (string)v.Value!).ToList();
            var values = valuesArg.Values.Select(v => v.Value!).ToList();

            sb.AppendLine($"        [ExcludeFromCodeCoverage]");
            sb.AppendLine($"        public class {name} : OpenApiExample<{type}>");
            sb.AppendLine("        {");
            sb.AppendLine($"            public override IOpenApiExample<{type}> Build(NamingStrategy namingStrategy = null!)");
            sb.AppendLine("            {");
            for (int i = 0; i < labels.Count; i++)
            {
                var val = type == "string" ? $"\"{values[i]}\"" : values[i].ToString();
                sb.AppendLine($"                Examples.Add(OpenApiExampleResolver.Resolve(\"{labels[i]}\", {val}, namingStrategy));");
            }
            sb.AppendLine("                return this;");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
        }
    }
}